// Exemplo 01 - Uso do switch... case
let flor: string = 'tulipa';

switch (flor) {
  case 'rosa':
    console.log('rosas são vermelhas');
    break;
  case 'violetas':
    console.log('violetas são azuis');
    break;
  case 'tulipa':
    console.log('tulipas são brancas');
    break;
  default:
    console.log('selecione uma outra flor');
}

// Exemplo 02 - Uso do switch... case
let diaUtilSemana: number = 5;

switch (diaUtilSemana) {
  case 0:
    console.log('domingo');
    break;
  case 1:
    console.log('segunda');
    break;
  case 2:
    console.log('terça');
    break;
  case 3:
    console.log('quarta');
    break;
  case 4:
    console.log('quinta');
    break;
  case 5:
    console.log('sexta');
    break;
  default:
    console.log('não é um dia util');
}
